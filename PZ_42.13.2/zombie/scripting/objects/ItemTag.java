    private final String translationName;

    private ItemTag(String translationName) {
        this.translationName = translationName;
    }

    public static ItemTag get(ResourceLocation id) {
        return Registries.ITEM_TAG.get(id);
    }

    @Override
    public String toString() {
        return Registries.ITEM_TAG.getLocation(this).toString();
    }

    public String getTranslationName() {
        return this.translationName;
    }

    public static ItemTag register(String id) {
        return register(false, id);
    }

    private static ItemTag registerBase(String id) {
        return register(true, id);
    }

    private static ItemTag register(boolean allowDefaultNamespace, String id) {
        return Registries.ITEM_TAG.register(RegistryReset.createLocation(id, allowDefaultNamespace), new ItemTag(id));
    }
